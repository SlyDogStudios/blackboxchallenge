loop_contrib:
	lda control_pad
	eor control_old
	and control_pad
	and #b_punch
	beq not_b_contrib
		lda #$20
		sta nametable
		jsr music_beep
		lda #<loop_title
		sta loop_pointer+0
		lda #>loop_title
		sta loop_pointer+1
		lda #<nmi_title
		sta nmi_pointer+0
		lda #>nmi_title
		sta nmi_pointer+1
