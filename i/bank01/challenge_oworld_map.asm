map_coords:
	ldx map_y
	lda map_x
	clc
	adc mult_lo, x
	sta temp_16bit_1+0
	lda mult_hi, x
	adc #$00
	sta temp_16bit_1+1

	lda #<bbc_oworld_map_lo
	clc
	adc temp_16bit_1+0
	sta temp_16bit_2+0
	lda #>bbc_oworld_map_lo
	adc temp_16bit_1+1
	sta temp_16bit_2+1

	lda #<bbc_oworld_map_hi
	clc
	adc temp_16bit_1+0
	sta temp_16bit_3+0
	lda #>bbc_oworld_map_hi
	adc temp_16bit_1+1
	sta temp_16bit_3+1

	ldy #$00
	lda (temp_16bit_2), y
	sta temp_16bit_1+0
	lda (temp_16bit_3), y
	sta temp_16bit_1+1

	lda is_beaten
	beq :+
		lda map_y
		cmp #$0f
		bne :+
			lda map_x
			cmp #$07
			bne :+
				lda #<oworld_beaten
				sta temp_16bit_1+0
				lda #>oworld_beaten
				sta temp_16bit_1+1
:
	rts

